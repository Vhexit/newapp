fun main(args: Array<String>) {
    for (number in 1..100) {

        if (number % 3 == 0) {
            println("$number Fizzbuzz")
        }
        else if (number % 5 == 0) {
            println("$number Buzz")
        }
        else if (number % 15 == 0) {
